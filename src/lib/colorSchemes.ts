export const DEFAULT_SCHEME_ID = "dispatcher-dark";

export const SCHEME_MAP = new Map<string, ColorScheme>(
  BUILTIN_SCHEMES.map((s) => [s.id, s])
);

export function getScheme(id: string): ColorScheme {
  return SCHEME_MAP.get(id) ?? SCHEME_MAP.get(DEFAULT_SCHEME_ID)!;
}

const UI_CSS_VAR_MAP: Record<keyof UIColors, string> = {
  bgPrimary: "--bg-primary",
  bgSecondary: "--bg-secondary",
  bgSurface: "--bg-surface",
  bgHover: "--bg-hover",
  bgActive: "--bg-active",
  textPrimary: "--text-primary",
  textSecondary: "--text-secondary",
  textMuted: "--text-muted",
  borderColor: "--border-color",
  accent: "--accent",
  green: "--green",
  red: "--red",
};

export function applyUIColors(colors: UIColors) {
  const style = document.documentElement.style;
  for (const [key, cssVar] of Object.entries(UI_CSS_VAR_MAP)) {
    style.setProperty(cssVar, colors[key as keyof UIColors]);
  }
}
